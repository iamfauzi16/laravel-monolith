    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $about = new About;
    
        $about->heading = $request->input('heading');
        $about->body = $request->input('body');
    
        if ($request->hasFile('images')) {
            $file = $request->file('images');
            $filename = time() . '_' . $file->getClientOriginalName();
            $path = $file->storeAs('public/images/abouts', $filename);
            $url = Storage::url($path);
            $about->images = $url;
        }
    
        $about->save();
    
        return redirect()->route('dashboard.content')->with('success', 'About page has been added successfully.');
    }

